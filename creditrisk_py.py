#Since montly income is skewed so fill values with median is safer than mean.
df['MonthlyIncome']= df['MonthlyIncome'].fillna(df['MonthlyIncome'].median())

#Filling number of dependents with 0(most common value)
df['NumberOfDependents'] = df['NumberOfDependents'].fillna(0)

# Sanity-check: some rows have implausible values (age = 0, extreme outliers in DebtRatio/RevolvingUtilization)
df = df[df["age"] > 0]

# Cap extreme outliers at the 99th percentile so a few bad data points don't dominate the model
for col in ["RevolvingUtilizationOfUnsecuredLines", "DebtRatio"]:
    cap = df[col].quantile(0.99)
    df[col] = df[col].clip(upper=cap)

print(df.isnull().sum().sum(), "missing values remaining")
print(df.shape)

